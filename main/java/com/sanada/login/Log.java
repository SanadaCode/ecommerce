package com.sanada.login;

public class Log {
	
	String mail;
	String pass;
	
	public Log(String mail, String pass) {
		this.mail = mail;
		this.pass = pass;
	}

	public String getMail() {
		return mail;
	}

	public void setMail(String mail) {
		this.mail = mail;
	}

	public String getPass() {
		return pass;
	}

	public void setPass(String pass) {
		this.pass = pass;
	}
	
	
	

}
